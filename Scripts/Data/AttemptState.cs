/// <summary>
/// Enums for the state of the attempts of a lift.
/// </summary>
public enum AttemptState
{
    /// <summary>
    /// Not attempted.
    /// </summary>
    NotAttempted = 0,

    /// <summary>
    /// Failed attempt.
    /// </summary>
    Fail = 1,

    /// <summary>
    /// Successful attempt.
    /// </summary>
    Success = 2
}
